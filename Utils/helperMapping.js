// Common Functions
const extractMainCity = (zoneString) => {
  const withoutGMT = zoneString.replace(/^\(GMT[^\)]+\)\s*/, "");

  const cities = withoutGMT.split(",").map((c) => c.trim());

  if (["Brazil", "Calcutta", "Manaus", "Tegucigalpa"].includes(cities[0])) {
    const mapping = {
      "Brazil": "Brasilia",
      "Calcutta": "Kolkata",
      "Manaus": "Central Time (US & Canada)",
      "Tegucigalpa": "Central America"
    }

    return mapping[cities[0]]
  }

  return cities[0] || null;
};

function extractDate(value) {
  if (!value) return null;

  if (/^\d{4}-\d{2}-\d{2}$/.test(value)) {
    return value;
  }

  const date = new Date(value);
  if (!isNaN(date)) {
    return date.toISOString().split("T")[0];
  }

  return value;
}

